package com.dev.clases;

/**
 *
 * @author INCOS
 */
public class Propietario {
    //atributos
    private int idProp;
    private String nomProp;
    private String patProp;
    private String matProp;
    private String dirProp;
    private String telProp;
    
    private String codigo;
    private String mensaje;
    //constructor

    public Propietario() {
    }
    //getter y setter

    public int getIdProp() {
        return idProp;
    }

    public void setIdProp(int idProp) {
        this.idProp = idProp;
    }

    public String getNomProp() {
        return nomProp;
    }

    public void setNomProp(String nomProp) {
        this.nomProp = nomProp;
    }

    public String getPatProp() {
        return patProp;
    }

    public void setPatProp(String patProp) {
        this.patProp = patProp;
    }

    public String getMatProp() {
        return matProp;
    }

    public void setMatProp(String matProp) {
        this.matProp = matProp;
    }

    public String getDirProp() {
        return dirProp;
    }

    public void setDirProp(String dirProp) {
        this.dirProp = dirProp;
    }

    public String getTelProp() {
        return telProp;
    }

    public void setTelProp(String telProp) {
        this.telProp = telProp;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }

    public String getMensaje() {
        return mensaje;
    }

    public void setMensaje(String mensaje) {
        this.mensaje = mensaje;
    }
    
    
}
