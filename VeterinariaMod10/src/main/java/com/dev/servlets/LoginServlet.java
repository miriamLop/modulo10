package com.dev.servlets;

import com.dev.bean.UsuarioBean;
import com.dev.clases.UsuarioRol;
import jakarta.servlet.RequestDispatcher;
import java.io.IOException;
import java.io.PrintWriter;
import jakarta.servlet.ServletException;
import jakarta.servlet.annotation.WebServlet;
import jakarta.servlet.http.HttpServlet;
import jakarta.servlet.http.HttpServletRequest;
import jakarta.servlet.http.HttpServletResponse;
import jakarta.servlet.http.HttpSession;
import java.sql.SQLException;
import java.util.logging.Level;
import java.util.logging.Logger;

/**
 *
 * @author meli
 */
@WebServlet(name = "LoginServlet", urlPatterns = {"/loginServlet"})
public class LoginServlet extends HttpServlet {

    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
   
    protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, SQLException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            /* TODO output your page here. You may use following sample code. */
            RequestDispatcher requestDispatcher;
            UsuarioRol usuarioRol = new UsuarioRol();
          
            // validando usuario con el metodo validarUsuario
            UsuarioBean usuarioBean = new UsuarioBean();
            usuarioRol = usuarioBean.verificarValidez(request);
            if (usuarioRol == null) {
                System.out.println("Error no se encontro el usuario");
                  // recuperando usuario sesion
                HttpSession session = request.getSession();
                // seteando objeto en sesion
                session.setAttribute("usuarioRol", usuarioRol);
                // si no se encontró el usuario nos reenviara a la página loginError.jsp
                requestDispatcher = request.getRequestDispatcher("/loginError.jsp");
            } else {
                System.out.println("Usuario encontrado: "+usuarioRol.getCodigo());
                // recuperando usuario sesion
                HttpSession session = request.getSession();
                // seteando objeto en sesion
                session.setAttribute("usuarioRol", usuarioRol);
              
                // si todo salio correcto nos llevara a la pagina de bienvenida
                requestDispatcher = request.getRequestDispatcher("/index.jsp");
            }
            requestDispatcher.forward(request, response);

        }
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            processRequest(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(LoginServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            processRequest(request, response);
        } catch (SQLException ex) {
            Logger.getLogger(LoginServlet.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    /**
     * Returns a short description of the servlet.
     *
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }// </editor-fold>

}
